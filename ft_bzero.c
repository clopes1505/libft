#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			k;
	unsigned char	*y;

	k = 0;
	y = (unsigned char*)s;
	while (k < n)
	{
		y[k] = '\0';
		k++;
	}
}
