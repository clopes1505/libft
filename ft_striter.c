#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int k;

	k = 0;
	if (s == NULL || f == NULL)
		return ((void)NULL);
	while (s[k])
	{
		f(&s[k]);
		k++;
	}
}
