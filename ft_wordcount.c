#include "libft.h"

int		ft_wordcount(char const *s, char c)
{
	unsigned int i;

	i = 0;
	while (*s)
	{
		while (*s == c)
			s++;
		if (*s)
			i++;
		while (*s != c && *s)
			s++;
	}
	return (i);
}
