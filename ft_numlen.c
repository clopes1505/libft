#include "libft.h"

int		ft_numlen(long n)
{
	int k;

	k = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		k++;
		n *= -1;
	}
	while (n > 0)
	{
		n /= 10;
		k++;
	}
	return (k);
}
