char	*ftstrncpy(char *src, char *dst, size_t len)
{
	int k;
	k = 0;
	while(dst[k] && k < len)
	{
		dst[k] =  src[k];
		k++;
	}	
	dst[k] = '\0';			
	return(dst);
}
