#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int k;

	k = 0;
	if (s == NULL || f == NULL)
		return ((void)NULL);
	while (s[k])
	{
		f(k, &s[k]);
		k++;
	}
}
