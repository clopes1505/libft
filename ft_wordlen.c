#include "libft.h"

int		ft_wordlen(char const *s, char c)
{
	int i;
	int k;

	k = 0;
	i = 0;
	while (s[k] == c)
		k++;
	while (s[k] != c && s[k] != '\0')
	{
		i++;
		k++;
	}
	return (i);
}
